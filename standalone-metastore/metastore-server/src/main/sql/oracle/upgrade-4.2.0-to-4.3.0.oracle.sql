  CTC_WRITEID NUMBER(19),
  CTC_UPDATE_DELETE CHAR(1) NOT NULL,
  CONSTRAINT TMP_COMPLETED_TXN_COMPONENTS_PK PRIMARY KEY (CTC_ID)
) ROWDEPENDENCIES;
INSERT INTO TMP_COMPLETED_TXN_COMPONENTS (CTC_TXNID, CTC_DATABASE, CTC_TABLE, CTC_PARTITION, CTC_TIMESTAMP, CTC_WRITEID, CTC_UPDATE_DELETE)
  SELECT CTC_TXNID, CTC_DATABASE, CTC_TABLE, CTC_PARTITION, CTC_TIMESTAMP, CTC_WRITEID, CTC_UPDATE_DELETE FROM COMPLETED_TXN_COMPONENTS;
DROP TABLE COMPLETED_TXN_COMPONENTS;
ALTER TABLE TMP_COMPLETED_TXN_COMPONENTS RENAME TO COMPLETED_TXN_COMPONENTS;
CREATE INDEX COMPLETED_TXN_COMPONENTS_INDEX ON COMPLETED_TXN_COMPONENTS (CTC_DATABASE, CTC_TABLE, CTC_PARTITION);

