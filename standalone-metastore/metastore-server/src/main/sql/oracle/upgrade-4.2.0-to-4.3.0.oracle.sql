-- Add surrogate primary keys for HA database replication. Oracle IDENTITY columns
-- do not backfill existing rows when added via ALTER TABLE, so rebuild each
-- populated table via a TMP_* copy/swap (same pattern as counter tables below).
-- Plan a maintenance window: each swap locks the table and scales with row count.

CREATE TABLE TMP_TXN_COMPONENTS (
  TC_ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  TC_TXNID NUMBER(19) NOT NULL,
  TC_DATABASE VARCHAR2(128) NOT NULL,
  TC_TABLE VARCHAR2(256),
  TC_PARTITION VARCHAR2(767) NULL,
  TC_OPERATION_TYPE char(1) NOT NULL,
  TC_WRITEID NUMBER(19),
  CONSTRAINT TMP_TXN_COMPONENTS_PK PRIMARY KEY (TC_ID)
) ROWDEPENDENCIES;
INSERT INTO TMP_TXN_COMPONENTS (TC_TXNID, TC_DATABASE, TC_TABLE, TC_PARTITION, TC_OPERATION_TYPE, TC_WRITEID)
  SELECT TC_TXNID, TC_DATABASE, TC_TABLE, TC_PARTITION, TC_OPERATION_TYPE, TC_WRITEID FROM TXN_COMPONENTS;
DROP TABLE TXN_COMPONENTS;
ALTER TABLE TMP_TXN_COMPONENTS RENAME TO TXN_COMPONENTS;
ALTER TABLE TXN_COMPONENTS ADD CONSTRAINT TXN_COMPONENTS_FK1 FOREIGN KEY (TC_TXNID) REFERENCES TXNS (TXN_ID);
CREATE INDEX TC_TXNID_INDEX ON TXN_COMPONENTS (TC_TXNID);

CREATE TABLE TMP_COMPLETED_TXN_COMPONENTS (
  CTC_ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  CTC_TXNID NUMBER(19) NOT NULL,
